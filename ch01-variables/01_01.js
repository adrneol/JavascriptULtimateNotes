// JavaScript is a dynamically typed language: a variable can hold any type and be reassigned.
// `let` declares a block-scoped variable (preferred over `var` in modern code).
let a = "me" // `a` is initially a string
console.log(a) // outputs: me

// Reassign `a` to a different type (number). This is allowed in dynamic typing.
a = 10 // `a` is now a number
console.log(a) // outputs: 10