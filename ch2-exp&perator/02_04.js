// logical operators
let logic1 = true; let logic2 = false;
console.log("logic1 && logic2:", logic1 && logic2); //false
console.log("logic1 || logic2:", logic1 || logic2); //true
console.log("!logic1:", !logic1); //false

let logicA = 10; let logicB = 20;
console.log("(logicA < 15) && (logicB > 15):", (logicA < 15) && (logicB > 15)); //true
console.log("(logicA > 15) || (logicB > 15):", (logicA > 15) || (logicB > 15)); //true
console.log("!(logicA === 10):", !(logicA === 10)); //false